package client

import "encoding/base64"

///////////////////////////////////////////////////////////////////////////////
// TYPES

type Token struct {
	Scheme string
	Value  string
}

///////////////////////////////////////////////////////////////////////////////
// GLOBALS

const (
	Bearer = "Bearer"
)

///////////////////////////////////////////////////////////////////////////////
// STRINGIFY

// Stringify the token value
func (token Token) String() string {
	return token.Scheme + " " + base64.RawStdEncoding.EncodeToString([]byte(token.Value))
}
