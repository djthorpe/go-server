package mdns

///////////////////////////////////////////////////////////////////////////////
// TYPES

type EventType uint

///////////////////////////////////////////////////////////////////////////////
// GLOBALS

const (
	None EventType = iota
	Receive
)

///////////////////////////////////////////////////////////////////////////////
// STRINGIFY

func (t EventType) String() string {
	switch t {
	case None:
		return "None"
	case Receive:
		return "Receive"
	}
	return "[?? Invalid EventType value]"
}
