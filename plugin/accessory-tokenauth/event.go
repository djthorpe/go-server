package main

///////////////////////////////////////////////////////////////////////////////
// TYPES

type Event uint

///////////////////////////////////////////////////////////////////////////////
// CONSTANTS

const (
	EventNone Event = iota
	EventCreateAdmin
)

///////////////////////////////////////////////////////////////////////////////
// STRINGIFY

func (e Event) String() string {
	switch e {
	case EventNone:
		return "EventNone"
	case EventCreateAdmin:
		return "EventCreateAdmin"
	default:
		return "[?? Invalid Event value]"
	}
}
